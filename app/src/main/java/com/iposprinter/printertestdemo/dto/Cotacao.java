package com.iposprinter.printertestdemo.dto;


public class Cotacao {

    private float valorHoraMoto;
    private float valorHoraCarro;
    private float cotacaoDolar;
    private float cotacaoPeso;
    private float cotacaoReal;
    private float  cotacaoGuarani;

    public float getValorHoraMoto() {
        return valorHoraMoto;
    }

    public void setValorHoraMoto(float valorHoraMoto) {
        this.valorHoraMoto = valorHoraMoto;
    }

    public float getValorHoraCarro() {
        return valorHoraCarro;
    }

    public void setValorHoraCarro(float valorHoraCarro) {
        this.valorHoraCarro = valorHoraCarro;
    }

    public float getCotacaoDolar() {
        return cotacaoDolar;
    }

    public void setCotacaoDolar(float cotacaoDolar) {
        this.cotacaoDolar = cotacaoDolar;
    }

    public float getCotacaoPeso() {
        return cotacaoPeso;
    }

    public void setCotacaoPeso(float cotacaoPeso) {
        this.cotacaoPeso = cotacaoPeso;
    }

    public float getCotacaoReal() {
        return cotacaoReal;
    }

    public void setCotacaoReal(float cotacaoReal) {
        this.cotacaoReal = cotacaoReal;
    }

    public float getCotacaoGuarani() {
        return cotacaoGuarani;
    }

    public void setCotacaoGuarani(float cotacaoGuarani) {
        this.cotacaoGuarani = cotacaoGuarani;
    }
}

